export function getCultureCardByWord(word: string) {
  return cultureCards.find(card =>
    card.word.toLowerCase() === word.toLowerCase()
  );
}

export function getRandomCultureCard() {
  return cultureCards[Math.floor(Math.random() * cultureCards.length)];
}
